function seededShuffle<T>(arr: T[], seed: number): T[] {
  const result = [...arr];
  let s = seed;
  for (let i = result.length - 1; i > 0; i--) {
    s = (s * 16807 + 0) % 2147483647;
    const j = s % (i + 1);
    [result[i], result[j]] = [result[j], result[i]];
  }
  return result;
}

function generateBody(title: string, excerpt: string, siteName: string, tags: string[]): string {
  return `${excerpt}\n\nIn today's competitive landscape, understanding and implementing the right strategies is what separates successful businesses from the rest. This guide breaks down everything you need to know about this topic and how ${siteName} can help you get ahead.\n\nKey takeaways:\n• Focus on delivering real value to your audience\n• Consistency compounds — small improvements lead to big results\n• Measure what matters and iterate based on data\n• Build trust through transparency and expertise\n\nWhether you're just getting started or looking to scale, the principles in this article will help you make informed decisions and see tangible results.\n\nTags: ${tags.map(t => `#${t}`).join(" ")}`;
}

export function getPostsForSite(siteType: string, siteName: string, siteId: number): SeoPost[] {
  const template = POST_TEMPLATES[siteType];
  if (!template) return [];

  const posts: SeoPost[] = [];
  const titleCount = template.titles.length;

  for (let round = 0; round < 10; round++) {
    const shuffledTitles = seededShuffle(template.titles, siteId * 100 + round * 7);
    const shuffledExcerpts = seededShuffle(template.excerpts, siteId * 200 + round * 13);

    for (let i = 0; i < titleCount; i++) {
      const title = round === 0
        ? shuffledTitles[i]
        : `${shuffledTitles[i]} — Part ${round + 1}`;
      const excerpt = shuffledExcerpts[i];
      posts.push({
        title,
        excerpt,
        body: generateBody(title, excerpt, siteName, template.tags),
        tags: template.tags,
      });
    }
  }

  return posts.slice(0, 200);
}

